	pubKey1 := PublicKeyWrapper{Object: RandPrivateKey().GetPublicKey()}
	pubKey2 := PublicKeyWrapper{Object: RandPrivateKey().GetPublicKey()}
	pubKey3 := PublicKeyWrapper{Object: RandPrivateKey().GetPublicKey()}

